#include <stdlib.h>

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!s)
		return ;
	while (s[i])
		i++;
	while (j <= i)
	{
		s[j] = '\0';
		j++;
	}
}
